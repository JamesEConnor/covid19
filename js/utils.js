function getBreakPoints(lines) {
    var sum = 0;
    var diff_sum = 0;
    
    var firstLine = true;
    
    lines.forEach(function(item, index) {
        if(firstLine) {
            firstLine = false;
        } else {        
            var cases = parseInt(item.split(',')[4]);
            console.log();
            sum += cases;
        }
    });
    
    var mean = Math.round(sum/lines.length);
    
    firstLine = true;
    lines.forEach(function(item, index) {
        if(firstLine) {
            firstLine = false;
        } else {        
            var cases = parseInt(item.split(',')[4]);
            console.log();
            diff_sum += (cases - mean)**2;
        }
    });
    
    var sd = Math.floor(diff_sum/(lines.length - 1));
    console.log(sd);
        
    return [mean - 2 * sd, mean - sd, mean, mean + sd, mean + 2 * sd]
}



//convertTo = 'date' to get MM/DD/YYYY, 'epoch' to get milliseconds since Jan. 1970
function dateConversion(toConvert, convertTo) {
    if(convertTo == 'date') {
        var d = new Date(toConvert);
        var month = d.getMonth().toString();
        month = month.length == 1 ? "0" + month : month;
        var day = d.getDate().toString();
        day = day.length == 1 ? "0" + day : day;
        
        return month + "/" + day + "/" + d.getFullYear();
    } else if(convertTo == 'epoch') {
        return Date.parse(toConvert).getTime();
    }
}